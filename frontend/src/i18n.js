// Default context (used by any component rendered without a LangProvider,
// e.g. isolated component tests) mirrors the provider's initial "en" state
// exactly, so those tests see the same English text either way.
const LangContext = createContext({
  lang: "en",
  t: (key, vars) => format(STRINGS.en[key] ?? key, vars),
  setLang: () => {},
});

export function LangProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem("onestatus.lang") || "en");
  useEffect(() => {
    localStorage.setItem("onestatus.lang", lang);
  }, [lang]);
  const t = (key, vars) => format(STRINGS[lang]?.[key] ?? STRINGS.en[key] ?? key, vars);
  return React.createElement(LangContext.Provider, { value: { lang, t, setLang } }, children);
}

export function useLang() {
  return useContext(LangContext);
}
